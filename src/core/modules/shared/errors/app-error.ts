export class AppError extends Error {
	public readonly statusCode: number;
	public readonly code: ErrorCode;

	constructor(message: string, statusCode: number, code: ErrorCode) {
		super(message);
		this.statusCode = statusCode;
		this.code = code;
		this.name = "AppError";
	}
}
